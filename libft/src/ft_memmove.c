#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned const char		*s;
	unsigned char			*d;
	size_t					i;

	s = src;
	d = dest;
	if (d == s)
		return (dest);
	if (d < s)
		ft_memcpy(dest, src, n);
	else
	{
		i = n;
		while (i > 0)
		{
			d[i - 1] = s[i - 1];
			i--;
		}
	}
	return (dest);
}
